package org.signalml.plugin.exception;


public class PluginAlgorithmDataException extends PluginException {

	/**
	 *
	 */
	private static final long serialVersionUID = 4772846823985593736L;

	public PluginAlgorithmDataException() {
		super();
	}

	public PluginAlgorithmDataException(String message) {
		super(message);
	}

	public PluginAlgorithmDataException(Throwable cause) {
		super(cause);
	}

	public PluginAlgorithmDataException(String message, Throwable cause) {
		super(message, cause);
	}

}
